    private void llenarTabla(CasaDeEventos casadeeventos){
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        
        Vector fila = new Vector();
        
        fila.add(casadeeventos.getNombre());
        fila.add(casadeeventos.getTematica());
        fila.add(casadeeventos.getNumeroDeInvitados());
        fila.add(casadeeventos.getDisponibilidadElFindeSemana());
        
        modelo.addRow(fila);
        
        JOptionPane.showMessageDialog(this, "Resgistro de la casa de eventos realizada!");

    }
