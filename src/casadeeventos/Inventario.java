        casadeeventos.setNombre(campoNombre.getText());
        campoNombre.setText("");
        
        casadeeventos.setTematica(campoTematica.getText());
        campoTematica.setText("");
        
        casadeeventos.setNumeroDeInvitados((Integer)campoInvitados.getValue());
        campoInvitados.setValue(0);
        
        casadeeventos.setDisponibilidadElFindeSemana(opcionSi.isSelected());
        
        llenarTabla(casadeeventos);
